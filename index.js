var lessons = [Start, Body, Weather, house, colors, food, clothes, family, animals]
var test=false;
var j = 0;
var numOfLesson = 0;
var cards = [ lessons[numOfLesson].slide0, lessons[numOfLesson].slide1, lessons[numOfLesson].slide2, lessons[numOfLesson].slide3, lessons[numOfLesson].slide4, lessons[numOfLesson].slide5, lessons[numOfLesson].slide6, lessons[numOfLesson].slide7]


function training(){
    test = true;
    
    for (var i = cards.length - 1; i > 0; i--) {
        var j = Math.floor(Math.random() * (i + 1));
        var temp = cards[i];
        cards[i] = cards[j];
        cards[j] = temp;
    }
    for (i=0; i<8;i++){
        
        document.getElementById('allcards').querySelectorAll('span')[i].innerText = '';
        
    }
    errors = 0;
    j=0;
    message = cards[0].senceEN;
    speak();
    return test,j;
}

function render(){
for (i=0; i<8;i++){
    cards = [ lessons[numOfLesson].slide0, lessons[numOfLesson].slide1, lessons[numOfLesson].slide2, lessons[numOfLesson].slide3, lessons[numOfLesson].slide4, lessons[numOfLesson].slide5, lessons[numOfLesson].slide6, lessons[numOfLesson].slide7]
    document.getElementById('allcards').querySelectorAll('img')[i].src = cards[i].src;
    document.getElementById('allcards').querySelectorAll('img')[i].className = cards[i].senceEN;
    document.getElementById('allcards').querySelectorAll('span')[i].innerText = cards[i].senceEN;
    
}
}
render()

document.getElementById('allcards').addEventListener('click', (e) =>{
    
    if(numOfLesson==0){
        numOfLesson = +e.target.className
        render()
    }
    
    var check = e.target.className
    console.log(check)  
      
    if (test == true){
        
        if(check == message){
            
            j++;
            if (j == 8){
                message=''
                j=0;
                test = false
                return alert('Training complete!' + '\n\nОшибок:' + errors);
                
            }
            message = cards[j].senceEN;
            speak();
            
        } else {
            errors++
            document.getElementById('count').innerText=errors
        }
    }
})


function speak(){
    speechSynthesis.speak(new SpeechSynthesisUtterance(message))
}
